    private void botonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAddActionPerformed
        String nom = textNombre.getText();
        String ape = textApe.getText();
        int dni = Integer.parseInt(textDni.getText());
        String ciu = textCiudad.getText();
        String dom = textDom.getText();
        String tel = textTel.getText();
        
        Cliente cl = new Cliente(dni, nom, ape, ciu, dom);
        
        Directorio directorio = new Directorio();
        
        directorio.agregarCliente(tel, cl);
    }//GEN-LAST:event_botonAddActionPerformed

